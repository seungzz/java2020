package day09;

public class Printer {
	
	
	int i;
	boolean b;
	double d;
	String s;
	
	
	Printer() {
		
	}
	
	void Println(int i){
		System.out.println(i);
		
	}
	void Println(boolean b) {
		System.out.println(b);
	}
	
	void Println(double d) {
		System.out.println(d);
	}
	
	void Println(String s) {
		System.out.println(s);
	}
}
