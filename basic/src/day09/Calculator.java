package day09;

public class Calculator {

	//메소드
	void powerOn() {
		System.out.println("전원을 켭니다.");
		//void가 있으면 아무 리턴값이 없어도 가능
		
	}
	
	int plus(int x, int y) {
		int result = x+y;
		return result;
	}
	
	double divide(int x, int y) {
		double result = (double)x/(double)y;
		return result;
	}
	
	void powerOff() {
		System.out.println("전원을 끕니다.");
	}
}
