package day03;

public class DenyLogicOperatorExample {
	public static void main(String[] args) {
		boolean play = true;
		System.out.println(play);
		
		play = !play;
		System.out.println(play);
		
		//!는 반전. 보통 boolean앞에 쓴다.
		
		
		play = !play;
		System.out.println(play);
	}

}
