package day01;

public class VariableExample {
	public static void main(String[] args) {
		//10을 변수 value의 초기 값으로 저장
		int value = 10;
		
		int result = value +10;
		//변수 value값을 읽고 10을 더하는 산술 연산을 수행
		//연산의 결과값을 변수 result 초기 값으로 저장
		
		System.out.println(value);
		System.out.println(result);
	}

}
